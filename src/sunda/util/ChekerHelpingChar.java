package sunda.util;

public class ChekerHelpingChar {
	public String cheker (String ngalagena) {
		switch (ngalagena) {
		case "a":
			return "\u1b83";
		case "i":
			return "\u1b84";
		case "u":
			return "\u1b85";
		case "e'":
			return "\u1b86";
		case "o":
			return "\u1b87";
		case "e":
			return "\u1b88";
		case "eu":
			return "\u1b889";
		case "k":
			return "\u1b8a" + "\u1baa";
		case "q":
			return "\u1b8b" + "\u1baa";
		case "g":
			return "\u1b8c" + "\u1baa";
		case "c":
			return "\u1b8e" + "\u1baa";
		case "j":
			return "\u1b8f" + "\u1baa";
		case "z":
			return "\u1b90" + "\u1baa";
		case "t":
			return "\u1b92" + "\u1baa";
		case "d":
			return "\u1b93" + "\u1baa";
		case "n":
			return "\u1b94" + "\u1baa";
		case "p":
			return "\u1b95" + "\u1baa";
		case "f":
			return "\u1b96" + "\u1baa";
		case "v":
			return "\u1b97" + "\u1baa";
		case "b":
			return "\u1b98" + "\u1baa";
		case "m":
			return "\u1b99" + "\u1baa";
		case "y":
			return "\u1b9a" + "\u1baa";
		case "r":
			return "\u1b9b" + "\u1baa";
		case "l":
			return "\u1b9c" + "\u1baa";
		case "w":
			return "\u1b9d" + "\u1baa";
		case "s":
			return "\u1b9e" + "\u1baa";
		case "x":
			return "\u1b9f" + "\u1baa";
		case "h":
			return "\u1ba0" + "\u1baa";
		case "0":
			return "\u1bb0";
		case "1":
			return "\u1bb1";
		case "2":
			return "\u1bb2";
		case "3":
			return "\u1bb3";
		case "4":
			return "\u1bb4";
		case "5":
			return "\u1bb5";
		case "6":
			return "\u1bb6";
		case "7":
			return "\u1bb7";
		case "8":
			return "\u1bb8";
		case "9":
			return "\u1bb9";
		}

		return ngalagena;
	}
}
